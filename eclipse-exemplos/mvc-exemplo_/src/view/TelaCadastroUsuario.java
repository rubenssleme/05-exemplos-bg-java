    private void jbuSalvarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuSalvarListaActionPerformed
        String nome = jtfNome.getText();
        String apelido = jtfApelido.getText();
        String senha =  jtfSenha.getText();
        String nivelAcesso  = jcbNivelAcesso.getSelectedItem().toString();
        Usuario usuario = new Usuario(nome, apelido, senha, Integer.parseInt(nivelAcesso));
       
        dados.inclui(usuario);
        
        jlaTotalUsuarios.setText(dados.numeroDeUsuario());
        
        System.out.println(usuario.toString());
        
         usuario = dados.limparCampos();
         
         jtfNome.setText(usuario.getNome());
         
         jtfNome.requestFocus();
    }//GEN-LAST:event_jbuSalvarListaActionPerformed

