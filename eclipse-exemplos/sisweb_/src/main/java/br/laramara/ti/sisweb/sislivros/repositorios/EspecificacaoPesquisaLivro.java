package br.laramara.ti.sisweb.sislivros.repositorios;

import br.laramara.ti.sisweb.utilitarios.NumeroUtils;

public class EspecificacaoPesquisaLivro {

	private Long id;
	private String contemTitulo;
	private String contemAutor;
	private String contemAssunto;
	private String contemLocalArmazenado;
	private String contemAaat;
	private String contemVolume;
	private String contemTipo;
	private boolean status;

	public Long getId() {
		return id;
	}

	public void setId(String id) {
		this.id = NumeroUtils.retornaLongoOuInvalido(id);
	}

	public String getContemTitulo() {
		return contemTitulo;
	}

	public void setContemTitulo(String contemTitulo) {
		this.contemTitulo = contemTitulo;
	}

	public void setContemAssunto(String contemAssunto) {
		this.contemAssunto = contemAssunto;
	}

	public String getContemAssunto() {
		return contemAssunto;
	}

	public String getContemAutor() {
		return contemAutor;
	}

	public void setContemAutor(String contemAutor) {
		this.contemAutor = contemAutor;
	}

	public String getContemLocalArmazenado() {
		return contemLocalArmazenado;
	}

	public void setContemLocalArmazenado(String contemLocalArmazenado) {
		this.contemLocalArmazenado = contemLocalArmazenado;
	}

	public String getContemAaat() {
		return contemAaat;
	}

	public void setContemAaat(String contemAaat) {
		this.contemAaat = contemAaat;
	}

	public String getContemVolume() {
		return contemVolume;
	}

	public void setContemVolume(String contemVolume) {
		this.contemVolume = contemVolume;
	}

	public String getContemTipo() {
		return contemTipo;
	}

	public void setContemTipo(String contemTipo) {
		this.contemTipo = contemTipo;
	}

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}

	public boolean possuiId() {
		return id != null;
	}

	public boolean possuiTitulo() {
		return contemTitulo != null;
	}

	public boolean possuiAutor() {
		return contemAutor != null;
	}

	public boolean possuiAssunto() {
		return contemAssunto != null;
	}

	public boolean possuiLocalArmazenado() {
		return contemLocalArmazenado != null;
	}

	public boolean possuiAaat() {
		return contemAaat != null;
	}

	public boolean possuiVolume() {
		return contemVolume != null;
	}

	public boolean possuiTipo() {
		return contemTipo != null;
	}

}
